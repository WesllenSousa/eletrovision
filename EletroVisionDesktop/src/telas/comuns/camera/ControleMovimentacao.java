    private void executar(Integer comando) {
        controlar(comando);
        controlar(++comando);
    }
    
    private void controlar(Integer comando) {
        try {
            HttpGet get = new HttpGet(MontarMensagens.urlCameraMovimentacao(camera.getIdentificacao(), 
                    camera.getPorta(), camera.getUsuario(), camera.getSenha(), comando));  
            HttpResponse response = cliente.execute(get);
            esperar(); 
            EntityUtils.consume(response.getEntity()); 
        } catch (IOException ex) {
        } 
    }
    
    private void esperar() {
        try {
            Thread.sleep(300);
        } catch (InterruptedException ex) {
        }
    }
    
